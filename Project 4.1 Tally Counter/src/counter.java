//Represents a tally counter
//****************************************************************************************
public class counter
{

	private int count;
	
	//------------------------------------------------------------------------------------
	//Sets initial counter value
	//------------------------------------------------------------------------------------
	
	public counter()
	{
		count = 0;
	}
	
	//------------------------------------------------------------------------------------
	//Adds 1 to the value of the counter
	//------------------------------------------------------------------------------------
	
	public int click()
	{
		count += 1;
		
		return count;
	}
	
	//------------------------------------------------------------------------------------
	//Returns the value of the counter
	//------------------------------------------------------------------------------------
	
	public int getCount()
	{
		return count;
	}
	
	//------------------------------------------------------------------------------------
	//Resets the counters value to 0
	//------------------------------------------------------------------------------------
	
	public int reset()
	{
		count = 0;
		
		return count;
	}


}
