//Represents a light bulb
//****************************************************************************************
public class Bulb
{

	private boolean state;
	
	//------------------------------------------------------------------------------------
	//Sets initial bulb state
	//------------------------------------------------------------------------------------
	
	public Bulb()
	{
		state = false;
	}
	
	//------------------------------------------------------------------------------------
	//Turns bulb on
	//------------------------------------------------------------------------------------
	
	public boolean turnon()
	{
		state = true;
		
		return state;
	}
	
	//------------------------------------------------------------------------------------
	//Turns bulb off
	//------------------------------------------------------------------------------------
	
	public boolean turnoff()
	{
		state = false;
		
		return state;
	}
	
	//------------------------------------------------------------------------------------
	//Returns the current state of the bulb(in true and false)
	//------------------------------------------------------------------------------------
	
	public boolean state()
	{
		return state;
	}
	
	public String status()
	{
		String io = null;
	
		
		if (state == true)
				io = "ON";
		else 
			io = "OFF";
		
		return io;
	}
}
